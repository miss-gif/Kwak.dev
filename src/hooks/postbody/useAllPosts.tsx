import { getAllPosts } from "@/api/postApi";
import { useQuery } from "@tanstack/react-query";

const useAllPosts = () => {
  const { isLoading, error, data } = useQuery({
    queryKey: ["posts"],
    queryFn: getAllPosts,
  });
  return { posts: data, isLoading, error };
};

export default useAllPosts;
